<h1>Un outil performant au service du milieu sportif</h1>
<p>Véritable lieu de vie, cette Maison propose aux Ligues et Comités, des locaux fonctionnels situés à l'Est de Nancy, permettant ainsi aux dirigeants, aux bénévoles et aux salariés d'échanger, de partager, de se former et de se regrouper dans des conditions optimales.
</p>
<p>Entièrement financé et géré par la Région Grand Est, un tel établissement est à la fois un facteur fort de cohésion et de qualité du sport régional.
</p>
<p>Il a pour vocation d'héberger les structures sportives régionales, de leur fournir des services administratifs, comptables et juridiques.
</p>
